-- ============================================================
-- 비밀번호 SHA-256 해시 마이그레이션
-- 로그인 로직이 평문 비교 → SHA-256 해시 비교로 변경됨에 따라
-- 기존 평문 비밀번호를 해시값으로 업데이트합니다.
--
-- 실행 전 주의사항:
--   1. Supabase SQL Editor에서 실행하세요.
--   2. pgcrypto 확장이 필요합니다 (아래 활성화 포함).
--   3. 모든 기존 사용자의 비밀번호가 아래 목록에 있어야 합니다.
--      누락된 사용자는 비밀번호 초기화가 필요합니다.
-- ============================================================

-- pgcrypto 확장 활성화 (SHA-256 계산용)
CREATE EXTENSION IF NOT EXISTS pgcrypto;

-- ============================================================
-- 현재 사용자 비밀번호 상태 확인 (실행 전 검토용)
-- ============================================================
-- SELECT id, username, name, length(password_hash) as pw_len, password_hash
-- FROM users ORDER BY created_at;

-- ============================================================
-- 기본 관리자 비밀번호 해시 업데이트
-- 기본값: admin / admin1234
-- ============================================================
UPDATE users
SET password_hash = encode(digest('admin1234', 'sha256'), 'hex')
WHERE username = 'admin'
  AND length(password_hash) != 64; -- 이미 64자(SHA-256 hex) 이면 건너뜀

-- ============================================================
-- 추가 사용자가 있는 경우 아래 패턴으로 업데이트하세요.
-- 예: UPDATE users SET password_hash = encode(digest('실제비밀번호', 'sha256'), 'hex')
--     WHERE username = '사용자명' AND length(password_hash) != 64;
-- ============================================================

-- ============================================================
-- 마이그레이션 결과 확인
-- ============================================================
SELECT
    username,
    name,
    role,
    CASE WHEN length(password_hash) = 64 THEN '✅ 해시됨' ELSE '❌ 평문 (업데이트 필요)' END AS password_status
FROM users
ORDER BY created_at;
